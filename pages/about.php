	<td colspan="2" class="copy" width="65%" rowspan="100%"><dd>AlgorithmA 2011 is a continuation of the CSCI 455 class at California State University, San Bernardino. AlgorithmA first started in 1991 and has gone through yearly iterations which entail updating, adding new algorithms, and reengineering code to update software design. AlgorithmA 2011 is adding five new algorithms which include heap, binary search, in-order traversal, post-order, and pre-order. A new authoring system will be implemented to allow users to write algorithms in pseudo code and see the result of their code animated in real time.</p>
<br>
<dd>CSCI 455 is a ten-week mock software company course that helps students display and animate the execution of algorithms through three main interfaces: Animation, Walkthrough, and Authoring. The animation interface animates an algorithm giving the user the ability to obtain an overall perspective of an algorithm's functionality.  The walkthrough interface allows the user the interactively step through an algorithm animation. The authoring interface allows the user to visualize arbitrary algorithms coded in a custom pseudo code. At the end of the course, the class culminates with a presentation, attended (voluntarily) by all levels of Computer Science students.  Frequently, members of years past return from the workforce to see how AlgorithmA has progressed since their year on the project.</p>
<br>
<dd>AlgorithmA is designed to help people new to computer programming understand structures available to them. We offer the animation as well as a summary of what is happening in order for someone to better understand. This originally was designed to help new students at CSUSB understand these structures in a more hands on way. On the open source it will be available for anyone to use.
